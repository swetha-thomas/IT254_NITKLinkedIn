function myFunction_experience() {
    var x = document.getElementById("experience");
    if (x.style.display === "none") {
      x.style.display = "block";
      y=document.getElementById("expbutton").innerHTML="Hide My Work Experience"
    } else {
      x.style.display = "none";
      y=document.getElementById("expbutton").innerHTML="Show My Work Experience"

    }
  }

